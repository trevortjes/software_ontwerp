#ifndef COLLORS_H_
#define COLLORS_H_


//--------------------------------------------------------------
// color designation
// 8bit color (R3G3B2)
// Red   (3bit) -> Bit7-Bit5
// Green (3bit) -> Bit4-Bit2
// Blue  (2bit) -> Bit1-Bit0
//--------------------------------------------------------------
#define  BLACK          0x00
#define  BLUE           0x03
#define  GREEN          0x1C
#define  RED            0xE0
#define  WHITE          0xFF

#define  CYAN           0x1F
#define  MAGENTA        0xE3
#define  YELLOW         0xFC


#endif /* COLLORS_H_ */
